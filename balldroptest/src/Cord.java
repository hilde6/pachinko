public class Cord {
 
    private int x=0;
    private int y=0;
     
    public Cord() {
 
    }
 
    public Cord(int x, int y) {
        this.x = x;
        this.y = y;
    }
 
    public int getX() {
        return x;
    }
 
    public int getY() {
        return y;
    }

}